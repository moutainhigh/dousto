package com.ibm.oms.admin.action.order.customer;

import java.util.Date;

public class Order {
	
	private long id;
	
	private String people;
	
	
	private Date confirmDateFrom;
	
	
	private String orderResource;


	public long getId() {
		return id;
	}


	public void setId(long id) {
		this.id = id;
	}


	public String getPeople() {
		return people;
	}


	public void setPeople(String people) {
		this.people = people;
	}


	public Date getConfirmDateFrom() {
		return confirmDateFrom;
	}


	public void setConfirmDateFrom(Date confirmDateFrom) {
		this.confirmDateFrom = confirmDateFrom;
	}


	public String getOrderResource() {
		return orderResource;
	}


	public void setOrderResource(String orderResource) {
		this.orderResource = orderResource;
	}
	
	
	
	

}
