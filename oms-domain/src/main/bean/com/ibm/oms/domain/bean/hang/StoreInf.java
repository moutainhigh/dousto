package com.ibm.oms.domain.bean.hang;

import java.io.Serializable;

/**
 * @author: mr.kai
 * @Description: 接口调用仓库信息实体类
 * @create: 2018-03-14 9:16
 **/
public class StoreInf implements Serializable {
    //订单编号
    private String orderNo;
    //发货门店名称
    private String shipStoreName;
    //供应地点(发货地址详情)
    private String provideAddress;
    //仓库编码
    private String warehouseNo;
    //发件方省份
    private String shipFromToProvince;
    //库存类型
    private String stockStatus;
    //发件方城市，市与 区不会同时为空
    private String shipFromCity;
    //发件方区县，市与 区不会同时为空
    private String shipFromArea;

    public StoreInf() {
    }

    public String getShipStoreName() {
        return shipStoreName;
    }

    public void setShipStoreName(String shipStoreName) {
        this.shipStoreName = shipStoreName;
    }

    public String getOrderNo() {
        return orderNo;
    }

    public void setOrderNo(String orderNo) {
        this.orderNo = orderNo;
    }

    public String getProvideAddress() {
        return provideAddress;
    }

    public void setProvideAddress(String provideAddress) {
        this.provideAddress = provideAddress;
    }

    public String getWarehouseNo() {
        return warehouseNo;
    }

    public void setWarehouseNo(String warehouseNo) {
        this.warehouseNo = warehouseNo;
    }

    public String getShipFromToProvince() {
        return shipFromToProvince;
    }

    public void setShipFromToProvince(String shipFromToProvince) {
        this.shipFromToProvince = shipFromToProvince;
    }

    public String getStockStatus() {
        return stockStatus;
    }

    public void setStockStatus(String stockStatus) {
        this.stockStatus = stockStatus;
    }

    public String getShipFromCity() {
        return shipFromCity;
    }

    public void setShipFromCity(String shipFromCity) {
        this.shipFromCity = shipFromCity;
    }

    public String getShipFromArea() {
        return shipFromArea;
    }

    public void setShipFromArea(String shipFromArea) {
        this.shipFromArea = shipFromArea;
    }
}
