package com.ibm.oms.intf.intf;

import java.io.Serializable;
import java.util.List;

/**
 * @author xiaonanxiang
 *
 */
public class CommodityStockInfoDTO implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * 商品code
	 */
	private String partNumber;
	
	/**
	 * sku编码
	 */
	private String skuCode;
	
	/**
	 * 条形码
	 */
	private String barCode;
	
	/**
	 * sku库存
	 */
	private String skuStockNum;
	
	private List<ColorSizeInfo> colorSizeInfos;
	

	public String getPartNumber() {
		return partNumber;
	}

	public void setPartNumber(String partNumber) {
		this.partNumber = partNumber;
	}

	public String getSkuCode() {
		return skuCode;
	}

	public void setSkuCode(String skuCode) {
		this.skuCode = skuCode;
	}

	public String getBarCode() {
		return barCode;
	}

	public void setBarCode(String barCode) {
		this.barCode = barCode;
	}

	public Long getSkuStockNum() {
		return Long.valueOf(skuStockNum);
	}

	public void setSkuStockNum(String skuStockNum) {
		this.skuStockNum = skuStockNum;
	}

	public List<ColorSizeInfo> getColorSizeInfos() {
		return colorSizeInfos;
	}

	public void setColorSizeInfos(List<ColorSizeInfo> colorSizeInfos) {
		this.colorSizeInfos = colorSizeInfos;
	}
}
