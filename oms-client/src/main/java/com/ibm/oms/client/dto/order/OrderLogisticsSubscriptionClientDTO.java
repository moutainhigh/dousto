package com.ibm.oms.client.dto.order;

import java.io.Serializable;

/**
 * Description: //模块目的、功能描述  
 * @author Yusl
 * Date:   2018年3月19日 
 */
public class OrderLogisticsSubscriptionClientDTO implements Serializable{

	/* 属性说明 */
	private static final long serialVersionUID = 1L;
	//快递公司
	private String company;
	//单号
	private String number;
	//出发地城市，省-市-区
	private String from;
	//目的地城市，省-市-区
	private String to;
	//授权码
	private String key;
    //回调接口
	private String callbackurl;

    //签名用随机字符串
	private String salt;
	//开通智能判断单号所属公司的功能
	private String autoCom;
	

	//添加此字段表示开启国际版
	private String interCom;

	//添加此字段表示开通行政区域解析功能
	private String resultv2;
	

	//返回的数据格式
	private String Schema;
	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getFrom() {
		return from;
	}

	public void setFrom(String from) {
		this.from = from;
	}

	public String getTo() {
		return to;
	}

	public void setTo(String to) {
		this.to = to;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getCallbackurl() {
		return callbackurl;
	}

	public void setCallbackurl(String callbackurl) {
		this.callbackurl = callbackurl;
	}

	public String getAutoCom() {
		return autoCom;
	}

	public void setAutoCom(String autoCom) {
		this.autoCom = autoCom;
	}

	public String getInterCom() {
		return interCom;
	}

	public void setInterCom(String interCom) {
		this.interCom = interCom;
	}

	public String getResultv2() {
		return resultv2;
	}

	public void setResultv2(String resultv2) {
		this.resultv2 = resultv2;
	}

	public String getSalt() {
		return salt;
	}

	public void setSalt(String salt) {
		this.salt = salt;
	}

	public String getSchema() {
		return Schema;
	}
			
	public void setSchema(String schema) {
		Schema = schema;
	}


	
	
}
