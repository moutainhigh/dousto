package com.ibm.oms.intf.intf;

import java.io.Serializable;

/**
 * 商品颜色、大小信息
 * @author xiaonanxiang
 *
 */
public class ColorSizeInfo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	/**
	 * 色码款名字(例如：颜色、大小)
	 */
	private String colorSizeName;
	
	/**
	 * 色码款值(例如：红色、蓝色、110、120)
	 */
	private String colorSizeValueName;

	public String getColorSizeName() {
		return colorSizeName;
	}

	public void setColorSizeName(String colorSizeName) {
		this.colorSizeName = colorSizeName;
	}

	public String getColorSizeValueName() {
		return colorSizeValueName;
	}

	public void setColorSizeValueName(String colorSizeValueName) {
		this.colorSizeValueName = colorSizeValueName;
	}
}
